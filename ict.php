        document.addEventListener('DOMContentLoaded', () => {
    // Modal Functionality
    const readMoreButtons = document.querySelectorAll('.read-more');
    const modals = document.querySelectorAll('.modal');
    const closeButtons = document.querySelectorAll('.close-btn');

    // Open Modal
    readMoreButtons.forEach(button => {
        button.addEventListener('click', (e) => {
            e.preventDefault();
            const modalId = button.getAttribute('data-modal');
            const modal = document.getElementById(modalId);
            if (modal) {
                // Added GSAP-like animation
                modal.style.display = 'block';
                modal.style.opacity = '0';
                modal.style.transform = 'scale(0.7)';
                
                // Smooth transition
                requestAnimationFrame(() => {
                    modal.style.transition = 'all 0.3s cubic-bezier(0.215, 0.610, 0.355, 1)';
                    modal.style.opacity = '1';
                    modal.style.transform = 'scale(1)';
                });
            }
        });
    });

    // Close Modal
    closeButtons.forEach(closeBtn => {
        closeBtn.addEventListener('click', () => {
            const modal = closeBtn.closest('.modal');
            if (modal) {
                // Added exit animation
                modal.style.opacity = '0';
                modal.style.transform = 'scale(0.7)';
                
                setTimeout(() => {
                    modal.style.display = 'none';
                    modal.style.transform = 'scale(1)';
                }, 300);
            }
        });
    });

    // Close Modal when clicking outside
    window.addEventListener('click', (e) => {
        if (e.target.classList.contains('modal')) {
            const modal = e.target;
            // Added exit animation
            modal.style.opacity = '0';
            modal.style.transform = 'scale(0.7)';
            
            setTimeout(() => {
                modal.style.display = 'none';
                modal.style.transform = 'scale(1)';
            }, 300);
        }
    });

    // Enhanced Fade In Animations with Staggered Reveal
    const fadeInElements = document.querySelectorAll('.fade-in');
    
    const observerOptions = {
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach((entry, index) => {
            if (entry.isIntersecting) {
                // Staggered animation with delay
                setTimeout(() => {
                    entry.target.style.transition = 'all 0.6s cubic-bezier(0.165, 0.84, 0.44, 1)';
                    entry.target.style.opacity = '0';
                    entry.target.style.transform = 'translateY(20px)';
                    
                    requestAnimationFrame(() => {
                        entry.target.style.opacity = '1';
                        entry.target.style.transform = 'translateY(0)';
                    });
                }, index * 100); // Stagger delay

                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    fadeInElements.forEach(element => {
        // Initial state
        element.style.opacity = '0';
        element.style.transform = 'translateY(20px)';
        observer.observe(element);
    });
});
    </script>
